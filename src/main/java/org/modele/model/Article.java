package org.modele.model;

public class Article {
	private String idarticle;
	private String title;
	private String doi;
	private String resume;
	private String motecle;
	private String issn;
	private String url;
	private String refernceArticle;
	
	public String getIssn() {
		return issn;
	}
	public void setIssn(String issn) {
		this.issn = issn;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getRefernceArticle() {
		return refernceArticle;
	}
	public void setRefernceArticle(String refernceArticle) {
		this.refernceArticle = refernceArticle;
	}
	public String getMotecle() {
		return motecle;
	}
	public void setMotecle(String motecle) {
		this.motecle = motecle;
	}
	public String getIdarticle() {
		return idarticle;
	}
	public void setIdarticle(String idarticle) {
		this.idarticle = idarticle;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDoi() {
		return doi;
	}
	public void setDoi(String doi) {
		this.doi = doi;
	}
	public String getResume() {
		return resume;
	}
	public void setResume(String resume) {
		this.resume = resume;
	}
	
	

}
