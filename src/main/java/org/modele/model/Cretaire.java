package org.modele.model;

public class Cretaire {
	private String nomC;
	private String descreptionC;
	private String nomM;
	
	public String getNomM() {
		return nomM;
	}
	public void setNomM(String nomM) {
		this.nomM = nomM;
	}
	public String getNomC() {
		return nomC;
	}
	public void setNomC(String nomC) {
		this.nomC = nomC;
	}
	public String getDescreptionC() {
		return descreptionC;
	}
	public void setDescreptionC(String descreptionC) {
		this.descreptionC = descreptionC;
	}
	
}
