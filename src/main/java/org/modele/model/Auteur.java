package org.modele.model;

public class Auteur {
private String idauterur;
private String nom;
private String prenom;
private String univers;
private String datedenaissance;
private String position;

public String getIdauterur() {
	return idauterur;
}
public void setIdauterur(String idauterur) {
	this.idauterur = idauterur;
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}
public String getPrenom() {
	return prenom;
}
public void setPrenom(String prenom) {
	this.prenom = prenom;
}
public String getUnivers() {
	return univers;
}
public void setUnivers(String univers) {
	this.univers = univers;
}
public String getDatedenaissance() {
	return datedenaissance;
}
public void setDatedenaissance(String datedenaissance) {
	this.datedenaissance = datedenaissance;
}
public String getPosition() {
	return position;
}
public void setPosition(String position) {
	this.position = position;
}

}
