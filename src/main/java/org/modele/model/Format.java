package org.modele.model;

public class Format {
	String type;
    String NCSD;
    String NDSC;
    
	public String getNDSC() {
		return NDSC;
	}

	public void setNDSC(String nDSC) {
		NDSC = nDSC;
	}

	public String getNCSD() {
		return NCSD;
	}

	public void setNCSD(String nCSD) {
		NCSD = nCSD;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	

}
